    public AddFacultyWindow(DataContainer datacontainer) {
        initComponents();
        this.datacontainer = datacontainer;
        
        // Add all the classrooms from the data container list to drop down on 
        // the form using a data model
        ArrayList<OfferedClass> listOfClasses = this.datacontainer.getListOfClasses();
        for (OfferedClass aClass : listOfClasses) {
            comboBoxModel.addElement(aClass.getClassName());
        }
        
        // Attach the list model to the drop down on the form
        this.selectClassesComboBox.setModel(comboBoxModel);
    }
    
