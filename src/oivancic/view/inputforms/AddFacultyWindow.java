    private void addClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addClassButtonActionPerformed
        // Retrieve selected class from the combo box and use it to create a facultyclass
       String selectedClass = (String) this.selectClassesComboBox.getSelectedItem();
       FacultyClass newFacultyClass = new FacultyClass();
       ArrayList<OfferedClass> offeredClasses = new ArrayList<OfferedClass>();
       
       // Retrieve the correct OfferedClass from the datacontainer and link it to this FacultyClass
        for (OfferedClass anOfferedClass : offeredClasses) {
            if (anOfferedClass.getClassName().equals(selectedClass)) {
                newFacultyClass.setClassIdNumber(anOfferedClass.getClassIdNumber());
                newFacultyClass.setClassName(anOfferedClass.getClassName());
                newFacultyClass.setClassroom(anOfferedClass.getClassroom());
            }
        }
       
       // Retrieve the SSN for the last saved student
       String ssn = this.newFacultyMember.getSocialSecurityNumber();
       
       // Open an AddStudentsToFacultyClass window and pass the facultyClass object to it
       AddStudentsToFacultyClass addEnrolledStudentsWindow = new AddStudentsToFacultyClass(datacontainer, newFacultyClass, ssn);
       addEnrolledStudentsWindow.setVisible(true);
    }//GEN-LAST:event_addClassButtonActionPerformed


