    private void saveFacultyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFacultyButtonActionPerformed
        // create a Faculty object
        newFacultyMember = new Faculty();
        
        // Retrive the data from the form
        String address = this.FacultyAddressField.getText();
        String socialSecurityNumber = this.FacultySocialSecurityNumberField.getText();
        String name = this.FacultytNameField.getText();
        String dayOfBirth = this.facultyDayOfBirthField.getText();
        String dayOfHire = this.facultyDayOfHireField.getText();
        String dayOfTermination = this.facultyDayOfTerminationField.getText();
        String monthOfBirth = this.facultyMonthOfBirthField.getText();
        String monthOfHire = this.facultyMonthOfHireField.getText();
        String monthOfTermination = this.facultyMonthOfTerminationField.getText();
        String salary = this.facultySalaryField.getText();
        String statusSelected = (String) this.facultyStatusComboBox.getSelectedItem();
        String yearOfBirth = this.facultyYearOfBirthField.getText();
        String yearOfHire = this.facultyYearOfHireField.getText();
        String yearOfTermination = this.facultyYearOfTerminationField.getText();
        
        // Convert variables to the variable types appropriate for an object of 
        // Faculty type. Store data in the created Faculty object
        newFacultyMember.setAddress(address);
        newFacultyMember.setSocialSecurityNumber(socialSecurityNumber);
        newFacultyMember.setName(name);
        
        int intDayOfBirth = Integer.parseInt(dayOfBirth);
        int intMonthOfBirth = Integer.parseInt(monthOfBirth);
        int intYearOfBirth = Integer.parseInt(yearOfBirth);
        newFacultyMember.setDateOfBirth(intYearOfBirth, intMonthOfBirth, intDayOfBirth);
        
        int intDayOfHire = Integer.parseInt(dayOfHire);
        int intMonthOfHire = Integer.parseInt(monthOfHire);
        int intYearOfHire = Integer.parseInt(yearOfHire);
        newFacultyMember.setDateOfHire(intYearOfHire, intMonthOfHire, intDayOfHire);
        
        int intDayOfTermination = Integer.parseInt(dayOfTermination);
        int intMonthOfTermination = Integer.parseInt(monthOfTermination);
        int intYearOfTermination = Integer.parseInt(yearOfTermination);
        newFacultyMember.setDateOfTermination(intYearOfTermination, intMonthOfTermination, intDayOfTermination);
        
        newFacultyMember.setSalary(Double.parseDouble(salary));
        
        char status = statusSelected.charAt(0);
        newFacultyMember.setStatus(status);
        
        // Save the data in the datacontainer
        datacontainer.getListOfFaculty().add(newFacultyMember);
        
        // Enable AddClass button
        this.addClassButton.setEnabled(true);
        
        // Clear all fields for the next entry
        clearAllFields();
        
    }//GEN-LAST:event_saveFacultyButtonActionPerformed

